class GalToLit {
	public static void main(String[] args) {
		double gallons; // хранит количество галонов
		double liters; // хранит результат преобразования в литры
		gallons=10; // начать с 10 галонов
		liters=gallons*3.7854; // преобразование в литры
		System.out.println(gallons + "галонов соответсвует" +liters + "литрам.");
		} 
	}
